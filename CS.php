<h3>DEFINE:</h3>
<li>Computer Engineering is defined as the discipline that embodies the science and technology of design, construction, implementation, and maintenance of software and hardware components of modern computing systems and computer-controlled equipment.</li>
<li>The students enrolled in B. Tech. Computer Science and Engineering (4 year) would have an option to specialize in one the following emerging areas- Artificial Intelligence and Machine Learning Data Science and Big Data Analytics Cyber Security and Privacy Minimum Requirement for Specialization.</li>
<h3>DATA SCIENCE & ARTIFICIAL INTELLIGENCE [DS & AI]</h3>
<li>Data science is a multi-disciplinary field that uses scientific methods, processes, algorithms and systems to generate actionable insights from data. Artificial intelligence deals with development of computer systems able to perform tasks requiring human intelligence such as speech recognition, decision-making, and translation. Data science enables artificial intelligent systems to perform better with the help of provided data and insights.</li>
<li>Data Science and AI specialisation is powered by Microsoft and foreign universities. With labs and curriculum designed in association with them, students will work on cutting-edge tools and technologies to embrace the new trends. Availability of High-Performance computing system on the campus leads to real-world applications. Additional IoT labs will allow the creation of data sets and real-world applications that can be used for testing the algorithms and developing new techniques.</li>
<li>Data Science and AI are being increasingly used in a variety of applications. Some everyday examples are:</li>
•	Unlocking phone with your fingerprint – your phone uses fingerprint data and pattern recognition
•	Voice-activated smart assistants like Siri and Google Assistant
•	Shortest route planning.

