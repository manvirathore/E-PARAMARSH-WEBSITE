    <h2>placements of Sharda-University-Greater-Noida </h2>
    <p>Sharda University has started its 2022 placement drive. During the placement drive, the highest international & domestic salary stood at INR 48 LPA & INR 25 LPA. The highest salary increased by 60% as compared to the previous year. Students received an average salary of INR 5.65 LPA. 

        During Sharda University School of Engineering Placements 2022,  reputed recruiters like Unacademy, Dominus Labs, InMobi Pvt Ltd, Ohreee Ltd, Karza Technologies, Matus Business Pvt Ltd, Zop Smart etc visited the campus. The higest international salary was offered by Bominus Labs & the highes domestic highest salary was offered by Unacademy. A total of 36 students from the School of Engineering & Technology got placed.
        
        During Sharda University Placements 2021, more than 350 recruiters visited the campus, among which 30+ recruiters were Fortune 500 recruiters. The highest salary stood at INR 30 LPA. MBA students received received the highest salary of INR 17.75 LPA.</p>
