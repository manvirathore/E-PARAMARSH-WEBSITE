<h3>Electronics and Communication Engineering Eligibility:</h3>
<li>The basic eligibility criteria for B.Tech in Electronics and Communication Engineering is, it's necessary for the candidate to complete class 12 with 50 per cent of aggregate marks from a recognized board.</li>
<li>Candidates appearing for the class 12th exam can also apply for the course.</li>
<li>Candidates need to qualify for the entrance exam held by the colleges or universities.</li>
<li>The age of the candidate should be in between 17 years to 21 years.</li>
<li>Physics, Maths, Chemistry are core subjects.</li>
<li>The required percentage of marks may have a slight change depending on the colleges. Also, there may be some other equivalent eligibility criteria for admission.</li>
