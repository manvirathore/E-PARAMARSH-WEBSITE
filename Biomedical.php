        <h3>DEFINE:</h3>
<li>Biomedical engineering (BME) or medical engineering is the application of engineering principles and design concepts to medicine and biology for healthcare purposes (e.g., diagnostic or therapeutic). BME is also traditionally known as " bioengineering ", but this term has come to also refer to biological engineering.</li> 
<h3>Eligibility Criteria and Courses after 12th</h3>
<li>Candidates must have passed Class 12 with Science stream or its equivalent from any recognized board in India.</li>
<li>Candidates must have a minimum of 50 per cent aggregate or more in Class 12 with science stream.</li>
<li>Reserved categories such as SC/ ST are given relaxation upto 5 per cent.</li>
<li>The qualifying examination must be with Physics, Maths, Chemistryand an extra subject of the student's choice.</li>
