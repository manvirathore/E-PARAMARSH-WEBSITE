<h3>JOB PROFILES:</h3>
Job Profile	
<h4>Structural Engineer:</h4>
They design, plan and supervise the construction of infrastructural projects like buildings, bridges, and so on. They also take care of alteration and extension to existing properties or structures.	The national average salary in India for Structural Engineers  is around INR 4-5   Lakhs for 1-4 years of experience as per Payscale.com
<h4>Geotechnical Engineers:</h4>
The primary responsibility of Geotechnical engineers is to create projects and find solutions to the problems that are related to geology.	The  average salary for Geotechnical Engineers  is around INR 3-4   Lakhs for a  0-1 year of experience as per Payscale.com
<h4>Site Engineers:</h4>
The take responsibility for security, safety, health, organizing, supervising material and people working at the site. They are part of the site management team.	The  average salary for site Engineers  is around INR 2.5-3   Lakhs for a 0-1 year of experience as per Payscale.com
<h4>Construction Engineers:</h4>
The main task includes - to plan, direct, and supervise large construction projects. They act as leader and middleman on construction projects and ensure everything about the project is safe, smooth and structurally sound.	The  average salary for Construction Engineers  is around INR 3.5-4   Lakhs for a 0-1 year of experience as per Payscale.com
