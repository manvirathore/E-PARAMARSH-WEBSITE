        <h3>DEFINE:</h3>
<li>Mechanical engineering is an engineering branch that combines engineering physics and mathematics principles with materials science, to design, analyze, manufacture, and maintain mechanical systems.It is one of the oldest and broadest of the engineering branches .</li>
<h3>ELIGIBILITY CRITERIA AFTER 12TH:</h3>
<li>BE/B.Tech in mechanical engineering is a four-year programme. Candidates who have completed Xth and XIIth or an equivalent examination with 55 per cent of marks in Physics, Chemistry and Mathematics from a recognised board are eligible for this course. Also, candidates who have completed three years diploma in any stream from a recognised board are eligible for this course.</li>
