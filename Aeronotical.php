        <h3>DEFINE:</h3>
<li>Aeronautical engineering deals with aircraft design and engineering for flight within the atmosphere. Aerospace engineering deals with engineering for air flight or space flight. All aircraft are complex systems that must operate in a variety of flight conditions and situations.</li>
<ul>
<h3>Eligibility Criteria and Courses after 12th</h3>
As an aspirant of Aerospace engineering, you must meet the minimum criteria mentioned below:

<li> For admission in top aerospace engineering colleges, you need a minimum of 60% marks in Class 12 having Physics, Chemistry, and Maths as subjects.</li>
<li>Admissions are also based on entrance examinations like JEE Mains and Advanced,
 IIST Entrance Exam, VITEEE, SRMJEE, GATE, etc.</li>
<li>For pursuing Postgraduation and Doctoral programs, you need 60% marks in Bachelor’s degree (B.Tech) and a valid GATE score.</li>
<li>If you are a candidate with a four-year degree in Engineering, a five-year degree in Architecture or a Postgraduate degree in Mathematics/Science/Statistics/Computer Applications are also eligible to pursue Aerospace Engineering.</li>
<li>To join the NASA in the United States (US), you need to have a degree in a variety of disciplines like biomedical engineering,chemistry, ceramic engineering, industrial engineering, metallurgy, oceanography, and optical engineering. NASA also recommends having a Ph.D.</li>
</ul>
